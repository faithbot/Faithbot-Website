		<h2>CrypKey SDK V8</h2>

		<p>Our goal is to help you create excitement around your products, as each of these items translate into high-visibility, market-leading features you pass on to your customers. As always, do not hesitate to send your suggestions for further improvement. You can find a "Share your experience!" link for sending an email in the Crypkey SDK folder under the Start Menu. Our customers, and their customers, remain our best inspiration for new features.</p>

		<ol>
			<li>
				<h4>Microsoft Windows 8 and 8.1 support</h4>
				<p>Windows 8 brings some changes to the way applications and drivers are handled. CrypKey Version 8 has been extensively tested on Windows 8 and all issues have been resolved, resulting in robust, clean running CrypKey security. Have peace of mind that CrypKey products will run smoothly as Windows 8 becomes more widely used in today’s computing marketplace.</p>
			</li>
			<li>
				<h4><a href="vmprotect.html">CrypKey VMProtect - Virtual Machine Duplication Protection</a></h4>
				<p>This was, understandably, a highly requested feature. Virtual Machines have become very common in today's computing landscape, and they provide an easy way to copy software - just copy the whole virtual machine, and it's good to go on any other computer. With CrypKey VMGuard, your software can be thoroughly protected from this loophole.</p>
				<p>With CrypKey V8, we deliver this new and efective solution to detecting and protecting software from virtual machine duplication. Using the existing CrypKey Intelligent Hardware Detection system, V8 can seamlessly add this protection to existing CrypKey licenses with no disruption - the first time you use V8 on an existing license, you are protected.</p>
			</li>
			<li>
				<h4>TCPIP Network Licensing</h4>
				<p>V8 supports 2 types of enterprise network licensing - the previously supported Netbios method, and now TCPIP. As usual, we have made it very easy to switch to and use this form of network communication. Configuration of TCPIP can be as simple as clicking "Enable TCPIP" on your current configuration. We have added a few useful TCPIP configuration options.</p>
				<p>The main advantage to using TCPIP is that it can work in network configurations where the current default net bios communication does not. However, beta testers of TCPIP have given us feedback that this system is faster, easier, and more robust to use.</p>
				<p>Other TCPIP advantages, and full usage details, are currently found in the CrypKey TCPIP Quickstart Vx.x.doc, in the Documentation folder.</p>
			</li>
			<li>
				<h4>Microsoft Visual Studio 2011 and 2013 support</h4>
				<p>Each new version of MSVS brings subtle changes, and any issues caused by these changes have been fixed. CrypKey still remains to be one of the few Software Protection systems that offers libraries compiled and tested with every version of MSVS. We even ship a test program for every version of MSVS, so that you have a baseline way of checking any particular feature against each version of MSVS.</p>
			</li>
			<li>
				<h4>CASPER support for License File</h4>
				<p>The License File, introduced in CrypKey V7, extends the amount and types of license controls you can put in your applications. Custom controls can easily be created and integrated. As an optional feature of CrypKey V8, you can use CASPER to deliver license files, and all the new license features you create can be stored in the CASPER database.</p>
			</li>
			<li>
				<h4>EasyNetworkLicenseConfig now supports entering a SiteKey</h4>
				<p>EasyNetworkLicenseConfig.exe made it unnecessary to have 3 different installs for StandAlone, Server, and Client configurations. Instead, a single install could be easily set up on first time usage to act in any of these 3 modes. CrypKey V8 takes this setup one step further by allowing the user to enter a license with this application, completing the process in a natural and convenient manner. Configure AND License your application using this simple and easy-to-use application.</p>
			</li>
			<li>
				<h4>USBKeys support licenses hidden from the operating system</h4>
				<p>CrypKey USBKeys put the CrypKey License Information in a hidden directory named "DO_NOT_DELETE", but bad applications and users can still access and delete this information.</p>
				<p>CrypKey V8 supports an Internal Flash Memory USBKey, and writes license information to memory that is not available to the operating licensing system, making USBKey licensing highly secure and robust.</p>
				<p>This system is seamless and invisible in usage - just purchase internal flash memory USBKeys, and we will do the rest.</p>
			</li>
			<li>
				<h4>New Look and Feel</h4>
				<p>We have added a new skin to the Site Key Generator to make it more user friendly and eye appeasing to use.</p>
			</li>
		</ol>



