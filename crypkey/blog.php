				<h2 id="model-primer">Software Licensing Models - A Primer</h2>
			</header>

			<p>In an effort to protect and control software distribution, several distinct software licensing models have been developed. Each model has a unique way of granting the authorized end user access to the licensed software. Most of the license models being used today fall under one of the following three categories.</p>

			<h3>Node-locked license</h3>
			<p>This is one of the most common license models in use today, sometimes referred to as an "individual computer license" where, in effect, the license is "locked" to the authorized computer. This model provides a specific license for a stand-alone computer with the license locked to the hardware on that PC. The software must be individually installed, with the license being authorized on each device. The activation of this license may require Internet or an exchange of encrypted keys.</p>

			<p>This license model works well when delivering software to individual customers with one, or only a few, devices or to customers where they prefer to license each stand-alone computer separately.</p>

			<h3>Network license</h3>
			<p>The advantage of the network license over a node-locked license is that license authorization is deployed from a server, giving the network administrator a more efficient way to manage software licensing. Network licensing controls the number of "concurrent users" that can use the software in a company. More complex network licensing can involve having named users or having different numbers of users for different modules in the software.</p>

			<p>This license is preferred by companies who want to share a pool of licenses or who want a more centralized method of licensing software.</p>

			<h3>USB key and dongle based license</h3>
			<p>USB key and dongle licensing both provide a mechanism to lock the use of the software to a physical device plugged into the computer. In order to use the desired software, a matching and licensed USB key or dongle must be connected to the computer. Consequently, when the USB key or dongle is removed from the computer, so is the license authorization for that particular software.</p>

			<p>This model has the advantage of easy portability between computers. Several computers can have the software installed but only the one with the USB key or dongle plugged in will open the software.</p>

			<h3>Going hand-in-hand: the end user experience and security</h3>
			The above three licensing models provide effective means of delivering licensed software to authorized users while preventing incidences of unauthorized use. Software developers and distributors must evaluate their needs in respect to the environment in which their software will be deployed. The right choice will enable a secure and friendly end user experience.

