				<h2 id="hack-risk">What is the risk level of my software being hacked?</h2>
			</header>

			<p>Software is a form of intellectual property. As such, if the software is of value, it may be susceptible to theft, hacking and/or illegal use. Hence, it is crucial that part of your software development plan be devoted to strategies which deter or mitigate illegal use of software. The degree to which you implement protective measures will generally depend upon the software's actual or perceived level of risk.</p>

			<h3>Before talking about risk levels, let's first define the types of breeches as it pertains to software.</h3>

			<p>Illegal use of software typically takes one of the following forms (or combinations thereof):</p>
			<ol>
				<li>Reverse engineering</li>
				<li>Duplication and/or illegal distribution/sales</li>
				<li>Use by unauthorized or unlicensed users</li>
				<li>Use of unauthorized or unlicensed copies</li>
			</ol>

			<p>The level of risk will be determined by the relationship between the intrinsic value of your software and the nature of the market in which your software is sold/available.</p>

			<h3>Here are two risk scenarios at the extremes:</h3>

			<ol>
				<li>Highest risk - the software has high value and/or a high sales volume in a marketplace that offers little or no legal protection for intellectual property</li>
				<li>Lowest risk - the software has low value and/or a low sales volume in a marketplace that offers established enforcement of intellectual property rights</li>
			</ol>

			<h3>Some risk scenarios to consider:</h3>
			<ul>
				<li>Emerging markets offer opportunity - and higher risk</li>
				<li>Higher sales volume increases value - and risk</li>
				<li>Highly sought after software carries an increased level of risk</li>
				<li>No software which strives for (or is expected to have) a high value and/or high sales volume should enter the marketplace unprotected</li>
				<li>Hackers operating in high risk markets often do not recognize or respect geographic or legal boundaries, even stealing software from markets with established intellectual property rights with little or no recourse</li>
			</ul>

			<p><strong>Protecting your intellectual properties in any marketplace ensures your software maintains its value while (ideally) gaining sales. This strategy promotes the longevity of your software - and your company.<strong></p>
